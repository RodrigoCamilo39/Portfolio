// Duas expressoes resultar em TRUE (Aritmeticos/Relacionados/Logicos)
let verdadeira1 = 3 + 5 <= 8 && 12 > 8;
let verdadeira2 = 4 * 4 >= 16 || 3000 < 2500;

console.log(verdadeira1);
console.log(verdadeira2);

// Duas expressoes resultar em FALSE (Aritmeticos/Relacionados/Logicos)
let falso1 = 3000 + 3000 * 2 == 6000 || 8 * 8 == 8;
let falso2 = 3 + 2 + 2 + 1 == 8 && 1 > 10;

console.log(falso1);
console.log(falso2);

